package Loops;

public class SwitchStatement
{
	public static void main(String[] args)
	{
		int age = 20;
		
		switch(age)
		{
		case 20 : System.out.println("Naresh age is 20");
		break;
		case 21: System.out.println("Naresh age is 21");
		break;
		case 24 : System.out.println("Naresh age is 24");
		break;
		}
	}
}
