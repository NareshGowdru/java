package Practice;

public class Switch 
{
	public static void main(String[] args)
	{
	int age = 21;
	switch(age) {
	case 18:
		System.out.println("eligible for vote");
		break;
		default:
			if (age>=18)
			{ System.out.println("eligible for vote");
			}
			else
			{
				System.out.println("not eligible to vote");
			
			}
	}
		
	}
	

}

	

